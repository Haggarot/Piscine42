#include<unistd.h>

int	main(int ac, char **av)
{
	int	i;
	int	p;

	i = 0;
	p = 1;
	if (p <= ac)
	{
		while (av[p])
		{
			while (av[p][i])
			{
				write(1, &av[p][i], 1);
				i++;
			}
			write(1, "\n", 1);
			i = 0;
			p++;
		}
	}
}
